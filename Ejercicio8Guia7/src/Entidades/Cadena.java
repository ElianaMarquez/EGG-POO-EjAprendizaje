
package Entidades;


public class Cadena {
    
    //atributos
    private String cadena;
    private int longitud;
    
    //getter y setter

    public String getCadena() {
        return cadena;
    }

    public void setCadena(String cadena) {
        this.cadena = cadena;
    }

    public int getLongitud() {
        return longitud;
    }

    public void setLongitud(int longitud) {
        this.longitud = longitud;
    }
    
    
}
